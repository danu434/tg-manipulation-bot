# ========== БОТ ==========
bot = Bot(token=TELEGRAM_TOKEN)
dp = Dispatcher()

# ========== ВЕБ-СЕРВЕР ==========
async def health(request):
    return web.Response(text="OK")

async def start_webserver():
    app = web.Application()
    app.router.add_get("/", health)
    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, host="0.0.0.0", port=PORT)
    await site.start()

# ========== ХЭНДЛЕРЫ ==========
@dp.message(Command("start"))
async def start(message: types.Message):
    user_levels[message.from_user.id] = 1
    await message.answer(
        "👋 Привет! Это тренажёр распознавания манипуляций.\n\nВыбери уровень сложности:",
        reply_markup=get_levels_keyboard()
    )

@dp.message(Command("menu"))
async def menu(message: types.Message):
    await message.answer("Выбери уровень:", reply_markup=get_levels_keyboard())

@dp.message(F.text == "📘 Как отвечать")
async def help_handler(message: types.Message):
    await message.answer(HELP_TEXT)

@dp.message(F.text == "🔄 Сменить уровень")
async def change_level(message: types.Message):
    await message.answer("Выбери новый уровень:", reply_markup=get_levels_keyboard())

@dp.message(F.text.startswith("Уровень"))
async def choose_level(message: types.Message):
    try:
        parts = message.text.strip().split()
        if len(parts) != 2:
            return
        level = int(parts[1])
        if 1 <= level <= 9:
            user_levels[message.from_user.id] = level
            await message.answer(f"✅ Уровень {level} выбран. Генерирую пример...")
            await send_example(message, level)
        else:
            await message.answer("Уровень должен быть от 1 до 9")
    except:
        pass

@dp.message()
async def handle_answer(message: types.Message):
    level = user_levels.get(message.from_user.id, 1)
    example = user_last_example.get(message.from_user.id, "")
    
    await message.answer("🔍 Анализирую твой ответ...")
    
    try:
        response = await client.chat.completions.create(
            model="deepseek/deepseek-chat",
            messages=[{
                "role": "user",
                "content": get_check_prompt(level, message.text, example)
            }]
        )
        reply = response.choices[0].message.content
        await message.answer(reply)
    except Exception:
        await message.answer("❌ Ошибка проверки. Попробуй ещё раз.")

async def send_example(message: types.Message, level: int):
    try:
        response = await client.chat.completions.create(
            model="deepseek/deepseek-chat",
            messages=[{
                "role": "user",
                "content": get_generation_prompt(level)
            }]
        )
        example = response.choices[0].message.content
        user_last_example[message.from_user.id] = example
        await message.answer(example)
    except Exception:
        await message.answer("❌ Ошибка генерации. Попробуй ещё раз.")

async def main():
    await bot.delete_webhook(drop_pending_updates=True)
    await start_webserver()
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())
